#include<iostream>
#include<cmath>
using namespace std;
int main()
{
cout<<"Square root of given number is "<<sqrt(25)<<endl;
cout<<"Power of given number is "<<pow(5,2)<<endl;
cout<<"Possible integer value of given number is "<<ceil(5.005)<<endl;
/*ceil function return a value greater or equal 
to the given number*/

cout<<"Possible integer value of given number is "<<floor(5.005)<<endl;
/*floor function return a value less or equal 
to the given number*/
cout<<" The Cosine of given angle is  "<<cos(60)<<endl;
cout<<" The Sin of given angle is  "<<sin(60)<<endl;
cout<<" The Tangent of given angle is  "<<tan(60)<<endl;
cout<<" The exponential of given number is  "<<exp(6)<<endl;
cout<<" The logrithm of given number is  "<<log(5)<<endl;
return 0;
}